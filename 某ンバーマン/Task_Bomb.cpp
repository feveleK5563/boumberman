	//-------------------------------------------------------------------
	//未爆発時の更新
	void Object::NoBomb()
	{
		if (cntTime >= 135)
		{
			Kill();
		}
		else
		{
			image.animCnt = float(noBombTable[(cntTime / 15) % 4]);
			++cntTime;
		}
	}

